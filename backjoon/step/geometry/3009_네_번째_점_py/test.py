from solution import solve
from util.test_runner import run_fun_solution

test_cases = [
  # 1) (1,1), (1,2), (2,1) → (2,2)
  (
    "1 1\n"
    "1 2\n"
    "2 1\n",
    "2 2\n"
  ),
  # 2) (100,100), (100,200), (300,100) → (300,200)
  (
    "100 100\n"
    "100 200\n"
    "300 100\n",
    "300 200\n"
  ),
  # 3) (10,20), (30,20), (30,40) → (10,40)
  (
    "10 20\n"
    "30 20\n"
    "30 40\n",
    "10 40\n"
  ),
  # 4) (0,0), (0,10), (20,0) → (20,10)
  (
    "0 0\n"
    "0 10\n"
    "20 0\n",
    "20 10\n"
  ),
  # 5) (7,3), (7,8), (14,3) → (14,8)
  (
    "7 3\n"
    "7 8\n"
    "14 3\n",
    "14 8\n"
  ),
  # 6) same as 5) input 순서만 변경: (14,3), (7,3), (7,8) → (14,8)
  (
    "14 3\n"
    "7 3\n"
    "7 8\n",
    "14 8\n"
  ),
  # 7) (1000,1), (1,1), (1,1000) → (1000,1000)
  (
    "1000 1\n"
    "1 1\n"
    "1 1000\n",
    "1000 1000\n"
  ),
  # 8) (1,1000), (1000,1000), (1000,1) → (1,1)
  (
    "1 1000\n"
    "1000 1000\n"
    "1000 1\n",
    "1 1\n"
  ),
  # 9) (123,456), (123,789), (987,456) → (987,789)
  (
    "123 456\n"
    "123 789\n"
    "987 456\n",
    "987 789\n"
  ),
  # 10) (500,600), (700,800), (500,800) → (700,600)
  (
    "500 600\n"
    "700 800\n"
    "500 800\n",
    "700 600\n"
  ),
]


def test():
  for input_data, expected_output in test_cases:
    output = run_fun_solution(input_data, solve)
    assert output == expected_output, f"Input: {input_data!r} Expected: {expected_output!r}, Got: {output!r}"
